# Давай уявимо, що ти — чарівник , і тобі потрібно відкрити скриню зі скарбами, яка захищена заклинанням. Щоб зняти закляття, ти маєш вгадати код із 3 цифр. Кожного разу, коли ти вводиш цифру, я буду казати, чи вона правильна.

# Програма має:

# Загадати 3 випадкові цифри від 0 до 9.

# Дати тобі 3 спроби, щоб вгадати кожну з них по черзі.

# Після кожної спроби повідомити, чи правильна цифра.

# Якщо ти вгадаєш усі три цифри, скриня відчиниться!

# Якщо ні — закляття залишиться, і скарб не дістанеться.
import random
print("Чарівник каже: скриня зі скарбами закрита магічним кодом з 3 цифр")
print("У тебе є лише 3 спроби для кожної цифри")

code = [random.randint(0, 9) for _ in range(3)]
guessed = {}
for i in range(3):
    success = False
    for attempt in range(3):
        guess = int(input(f"Спроба {attempt+1}: введи {i+1}-шу цифру "))
        if guess == code[i]:
            print("Правильно!")
            guessed.append(guess)
            success = True
            break
        else:
            print("Неправильно!")
    if not success:
        print("Закляття занадто сильне... скарб залишається зачиненим")
        break
else:
    print("Ти вгадав усі цифри! Скриня відкрилась і скарб твій")    